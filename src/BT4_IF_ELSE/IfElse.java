package BT4_IF_ELSE;

public class IfElse {
    public static void main(String[] args) {
        int number = 100;

        if (number == 100) {
            System.out.println("Điều kiện đúng");
        } else if (number < 50) {
            System.out.println("Điều kiện sai");
        } else if (number > 50) {
            System.out.println("Điều kiện đúng");
        }
    }
}